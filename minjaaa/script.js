var a = prompt("Enter first number")
var b = prompt("Enter operation")
var c = prompt("Enter second number")

switch(b) {
    case "+":
        alert(parseInt(a) + parseInt(c));
        break;
    case "-":
        alert(parseInt(a) - parseInt(c));
        break;
    case "*":
        alert(parseInt(a) * parseInt(c));
        break;
    case "/":
        alert(parseInt(a) / parseInt(c));
        break;
}


    
 

   


